# 变量与对象
# 赋值 a = 100，是让变量 a 指向堆里的 100 对象，不是把值存在 a 里。
# 多个变量可以指向同一个对象，修改对象，所有指向它的变量都能看到变化。
# del a 只是删除引用，不直接释放内存；只有对象引用计数为 0，才会被回收。
# 对象
# 实例对象：存自己的实例属性 + 指向类的引用，不存方法代码。
# 方法都在类里，所有实例共享，调用时自动把当前实例传给 self。
# 类
# 类是 type 的对象，定义时就在堆区创建，生命周期通常和程序一致。
# 类属性存于类对象，所有实例共享；修改类属性，所有实例都受影响。
# 实例给同名 “类属性” 赋值，是新建实例属性，不会改类本身。
# 内存区域
# 栈区：存放变量（引用、地址），占用极小。
# 堆区：存放所有对象（实例、类、数字、字符串等），是数据真正所在。
# 最关键的三句结论
# 变量 = 引用 = 地址，不存数据。
#  对象 =数据实体，存在堆中。
# 类 = 模板对象，方法和类属性归它，实例共享